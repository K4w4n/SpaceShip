    GameConfig(int level) {
        enemyMatrix = levels[level];
    }

    private int[][] enemyMatrix;

    public ArrayList<Enemy> getEnemyList() {
        ArrayList<Enemy> enemyList = new ArrayList<Enemy>();

        for (int i = 0; i < enemyMatrix.length; i++) {
            int[] enemyRow = enemyMatrix[i];
            for (int j = 0; j < enemyRow.length; j++) {
                int enemyTipe = enemyRow[j];
                if (enemyTipe != 0) {
                    enemyList.add(new Enemy(i, j, enemyTipe));
                }
            }
        }

        return enemyList;
    }
